SELECT 
    p.Numero_Postulacion,
    p.Codigo_Postulacion,
    p.Fecha_Postulacion,
    p.Presupuesto_Total,
    e.Nombre_Empresa,
    e.Rut_Empresa,
    s.Nombre_Sede,
    re.Nombre_Region AS Region_Ejecucion,
    ri.Nombre_Region AS Region_Impacto,
    ti.Nombre_Tipo_Iniciativa,
    ep.Nombre_Estado
FROM Postulacion p
JOIN Entidad_Empresa e 
    ON p.Rut_Empresa = e.Rut_Empresa
JOIN Sede s 
    ON p.ID_Sede = s.ID_Sede
JOIN Region re
    ON p.ID_Region_Ejecucion = re.ID_Region
JOIN Region ri 
    ON p.ID_Region_Impacto = ri.ID_Region
JOIN Tipo_Iniciativa ti 
    ON p.ID_Tipo_Iniciativa = ti.ID_Tipo_Iniciativa
JOIN Estado_Postulacion ep 
    ON p.ID_Estado = ep.ID_Estado
WHERE p.Numero_Postulacion= 1;

SELECT 
    pi.Numero_Postulacion, tp.Nombre_Tipo_Persona,
    COUNT(ie.ID_integrante) AS Total_Integrantes
FROM Postulacion_Integrante pi
JOIN Integrante_Equipo ie 
    ON pi.ID_integrante = ie.ID_integrante
JOIN Tipo_Persona tp 
    ON ie.ID_Tipo_Persona = tp.ID_Tipo_Persona
GROUP BY pi.Numero_Postulacion, tp.Nombre_Tipo_Persona;